    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        if (txtplat.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (txtmerk.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (cbwarna.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (txttgl.getText().equals("")) {
            JOptionPane.showMessageDialog(this, " isi semua data !");
        } else {
            try (Connection conn = connection.getConnection()) {
                String sql = "INSERT INTO kendaraan (platnomor, merk, warna, tanggalproduksi) VALUES (?, ?, ?, ?)";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, txtplat.getText());
                pst.setString(2, txtmerk.getText());
                pst.setString(3, cbwarna.getSelectedItem().toString());
                pst.setString(4, txttgl.getText()); 
                pst.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                showTable();
                bersih();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (txtplat.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau dihapus!");
            return;
        }
        try {
            String sql = "DELETE FROM kendaraan WHERE platnomor=?";
            var ps = conn.prepareStatement(sql);
            ps.setString(1, txtplat.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            showTable();
            bersih();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error delete: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

