    public void showTable() {
        try {
            String sql = "SELECT * FROM kendaraan"; // sesuaikan nama tabel
            ResultSet rs = stmt.executeQuery(sql);

            // Buat model untuk JTable
            DefaultTableModel model = new DefaultTableModel(
                    new String[]{"Plat Nomor", "Merk", "Warna", "Tanggal Produksi"}, 0
            );

            // Masukkan data ke model
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("platnomor"),
                    rs.getString("merk"),
                    rs.getString("warna"),
                    rs.getString("tanggalproduksi")
                });
            }

            // Tampilkan model ke JTable
            tb1.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

